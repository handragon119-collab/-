def build_angles() -> list[dict]:
    """카테고리를 라운드로빈으로 섞어, 연속된 글이 서로 다른 카테고리가 되게 한다."""
    buckets = {k: list(v) for k, v in CATS.items()}
    order = ["A", "B", "C", "D", "E", "F"]
    angles = []
    while any(buckets.values()):
        for k in order:
            if buckets[k]:
                a = buckets[k].pop(0)
                angles.append({**a, "cat": k})
    return angles


ANGLES = build_angles()           # 36개
START = datetime.date(2026, 6, 13)
DAYS = 10
WINDOWS = [(6, 11), (13, 18), (18, 28)]  # 시작시(포함)~끝시(미포함), 28시=익일 04시


def main() -> None:
    random.seed(20260613)
    n = len(ANGLES)
    offset = n // len(ACCOUNTS)  # 계정 간 간격 → 같은 슬롯에 다른 글
    entries = []
    idx = 0
    for d in range(DAYS):
        date = datetime.datetime(START.year, START.month, START.day) + datetime.timedelta(days=d)
        for w, (h0, h1) in enumerate(WINDOWS):
            for a, acc in enumerate(ACCOUNTS):
                angle = ANGLES[(d * 3 + w + a * offset) % n]
                mins = random.randint(h0 * 60, h1 * 60 - 1)
                dt = date + datetime.timedelta(minutes=mins)
                closer = "" if angle["cat"] == "F" else CLOSERS[idx % len(CLOSERS)]
                pid = f"plan-{SHORT[acc]}-d{d:02d}-w{w}"
                paths = cardgen.render_post(acc, pid, angle["cards"], seed=idx,
                                            handle=HANDLE[acc])
                entries.append({
                    "id": pid, "username": acc, "topic": angle["topic"],
                    "run_at": dt.strftime("%Y-%m-%d %H:%M"),
                    "text": angle["cap"] + closer,
                    "image_files": paths,
                    "preview_urls": ["/" + p for p in paths],
                })
                idx += 1

    existing = []
    if PREPARED.exists():
        try:
            existing = json.loads(PREPARED.read_text(encoding="utf-8"))
        except json.JSONDecodeError:
            existing = []
    keep = [e for e in existing if not str(e.get("id", "")).startswith("plan-")]
    PREPARED.write_text(json.dumps(keep + entries, ensure_ascii=False, indent=1),
                        encoding="utf-8")
    print(f"✅ 플랜 {len(entries)}개 생성 (유지된 기존 글 {len(keep)}개)")
    for acc in ACCOUNTS:
        print(f"   @{acc}: {sum(1 for e in entries if e['username'] == acc)}개")


if __name__ == "__main__":
    main()
